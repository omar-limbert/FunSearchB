public class Convertor {

    public Convertor() {
    }

    public double convertGbToMb(double tam) {
        return tam * 1024.0;
    }

    public double convertMbToKb(double tam) {
        return tam * 1024.0;
    }

    public double convertKbToB(double tam) {
        return tam * 1024.0;
    }


}
